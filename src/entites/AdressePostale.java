package entites;

public class AdressePostale {

	int numeroRue;
	String libelléRue;
	int codePostal;
	String ville;
	
	public AdressePostale(int numeroRue, String libelléRue, int codePostal, String ville) {
		
		this.numeroRue = numeroRue;
		this.libelléRue = libelléRue;
		this.codePostal = codePostal;
		this.ville = ville;
	}
	
	
	
	
}
